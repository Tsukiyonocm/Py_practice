# Question 1 - Return the Sum of Two Numbers
# Create a function that takes two numbers as arguments
# and Return their sum.

# Example:
# addition(3, 2) ==> 5
# addition(-3, -6) ==> -9
# addition(7, 3) ==> 10

def addition(x, y):
    return x + y

